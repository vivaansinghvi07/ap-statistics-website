<?php startblock('notes') ?>
        <li>If the study includes a random sample, you are able to&nbsp;<strong>generalize&nbsp;</strong>the results to the population</li>
        <li>If the study is a randomly-assigned experiment, you can determine <strong>causation</strong></li>
        <li>Since some experiments are morally questionable, they lack realism and therefore lack a clear answer
            <ul>
                <li>However, given certain circumstances, you are able to make assumptions about causation from an observational study
                    <ul>
                        <li>The association must be strong</li>
                        <li>The association must be consistent</li>
                        <li>Larger values of the explanatory variable lead to larger values of the response variable</li>
                        <li>The cause precedes the effect</li>
                        <li>The conclusion is plausible</li>
                    </ul>
                </li>
                <li>For example, it is not morally right to conduct an experiment about smoking related to lung cancer
                    <ul>
                        <li>However, there exists enough research and data to meet the conditions listed above, so you can reasonably assume smoking leads to lung cancer despite the lack of an experiment</li>
                    </ul>
                </li>
            </ul>
        </li>
        <li>An important thing to consider when making an experiment is&nbsp;<strong>data ethics:</strong>
            <ul>
                <li>Studies must be reviewed in advance by an&nbsp;<strong>institutional review board (IRB),&nbsp;</strong>which makes sure the experiment is safe and plausible</li>
                <li>All individuals participating in the study must have <strong>informed consent</strong>
                    <ul>
                        <li>They must be willing to participate AND know what they are participating in</li>
                    </ul>
                </li>
                <li>All personally identifiable data must be kept completely confidential
                    <ul>
                        <li>Specific medical records and other personal information are all identifiable (you are able to link them to a person)</li>
                    </ul>
                </li>
            </ul>
        </li>
