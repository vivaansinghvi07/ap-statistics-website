<?php startblock('notes') ?>
        <li><strong>Bias&nbsp;</strong>in a study is when it consistently either overestimates or underestimates a value that it is trying to determine
            <ul>
                <li>There are several types of bias</li>
            </ul>
        </li>
        <li><strong>Nonresponse bias </strong>is when some of the selected individuals (through sampling) do not response to or refuse to participate in the study
            <ul>
                <li>The individuals that do respond may hold more passionate views on the topic, messing with the data</li>
            </ul>
        </li>
        <li><strong>Undercoverage bias</strong> is when the sapling method is unable to reach a certain group of individuals
            <ul>
                <li>For example, if you are sampling by calling landline phones, the households without landlines will be left out the survey entirely</li>
            </ul>
        </li>
        <li><strong>Response bias</strong> is when the respondent of a survey answers a question in an unintended manner, for whatever reason
            <ul>
                <li>It could be due to confusing wording of the question itself&nbsp;</li>
                <li>It could also be due to the respondent's own personal feelings (i.e. embarrassment)</li>
            </ul>
        </li>
